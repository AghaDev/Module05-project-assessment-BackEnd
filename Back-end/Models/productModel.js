const mongoose = require('mongoose');

const productSchema = new mongoose.Schema({
  imageURLs: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  timestamps: {
    created_at: {
      type: Date,
      default: Date.now,
    },
    updated_at: {
      type: Date,
      default: Date.now,
    },
  },
});

const Product = mongoose.model('Product', productSchema);

module.exports = Product;
